import Head from 'next/head'
import { useEffect, useState } from 'react'
import { useRouter } from 'next/router'
import styles from '../styles/Home.module.css'

export default function Results() {
  
  const [userData, setUserData] = useState('')
  const [isLoading, setIsLoading] = useState(false)

  let {validationId} = useRouter().query

  useEffect(()=>{
    setIsLoading(true)
    if (validationId != null) {
      fetch('api/validation?validationId='+validationId)
      .then( (response) => response.json() )
      .then( (response) => { 
        setUserData(response)
        setIsLoading(false)
      })
      .catch( (error) => console.log(error) )
    }
    
  },[useRouter()])    
  

  if (isLoading) return <p className={styles.description}>Cargando</p>
  if (!userData || !userData.details) return <p className={styles.description}>Por favor, recarga la p치gina</p>
  if (userData.validation_status == 'failure' ) return (<div className={styles.description}>
                                                          <p>Verificaci칩n fallida, aqu칤 m치s detalles:</p>
                                                          <ul className={styles.list}>
                                                            <li>Status: {userData.failure_status}</li>
                                                            <li>Motivo: {userData.declined_reason}</li>
                                                          </ul>
                                                        </div>)
  return (
    <div className={styles.container}>
      <Head>
        <title>Identify</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className={styles.main}>        
        <div className={styles.userCard}>
          <h1>
            { `${userData.details.document_details.name} ${userData.details.document_details.last_name}` }
          </h1>      
          <p className={styles.descriptionCard}>{ userData.validation_status == 'success' ? 'Validaci칩n exitosa' : '' }</p>
          <h2>Resultados de la verificaci칩n</h2>
          <ul className={styles.list}>
            <li><strong>Ciudad/Pa칤s</strong>: { `${userData.details.document_details.birth_place} - ${userData.details.document_details.country}` }</li>
            <li><strong>Sexo</strong>: { userData.details.document_details.gender == 'male' ? 'Masculino':'Femenino'}</li>
            <li><strong>Fecha nacimiento</strong>: { userData.details.document_details.date_of_birth }</li>
            <li><strong>Numero de documento</strong>: { userData.details.document_details.document_number }</li>
            <li><strong>Expedido en..</strong> { userData.details.document_details.expedition_place }</li>
          </ul>
        </div>
      </main>

      <footer className={styles.footer}>
        <a
          href="https://tuasesor.digital"
          target="_blank"
          rel="noopener noreferrer"
        >
          With 游눞 by Oscar Riascos
        </a>
      </footer>
    </div>
  )
}
